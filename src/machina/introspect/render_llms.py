def render_markdown(spine: Spine, git_sha: str = UNCOMMITTED_SHA) -> str:
    """Render a :class:`Spine` to the committed ``capabilities.md`` artifact.

    The document is, in order: the provenance header block, an intro, a
    capability x connector matrix, the seam manifest (protocols + conventions +
    add-connector template), and a config-schema summary. Every section
    iterates collections in the core's existing sort order — no new sorting is
    introduced — so the rendered body is deterministic for a given spine.

    Args:
        spine: The spine to render.
        git_sha: The commit SHA for the provenance header (default
            ``"uncommitted"``).

    Returns:
        The full Markdown artifact, provenance header included.
    """
    parts: list[str] = [
        render_provenance_header(git_sha),
        "",
        "# Machina capability matrix",
        "",
        (
            "Code-derived from `machina.introspect.describe()`. This file is "
            "**generated** — do not edit it by hand. Run `python "
            "scripts/gen_spine.py` to regenerate after changing connectors, "
            "capabilities, or seams."
        ),
        "",
        _render_matrix(spine.connectors, spine.capabilities),
        _render_capability_index(spine.capabilities),
        _render_seams(spine.seams),
        _render_config_summary(spine.config_schema),
        _render_gaps(spine),
    ]
    return "\n".join(parts).rstrip() + "\n"


def _render_matrix(
    connectors: tuple[ConnectorInfo, ...],
    capabilities: tuple[CapabilityInfo, ...],
) -> str:
    """Render the capability x connector matrix.

    A row per capability that at least one registered connector provides
    (live or configurable); a column per connector type. A cell is ``yes``
    (guaranteed live), ``cfg`` (configurable — needs the right config/backend),
    or empty. Capabilities with no registered provider are listed separately in
    the gaps section, never as an all-empty row.
    """
    conn_types = [c.type for c in connectors]
    # Map (capability_value, conn_type) -> "yes" | "cfg".
    cell: dict[tuple[str, str], str] = {}
    for conn in connectors:
        for cc in conn.capabilities:
            cell[(cc.capability, conn.type)] = "cfg" if cc.configurable else "yes"

    header = "| Capability | Method | " + " | ".join(conn_types) + " |"
    divider = "| --- | --- | " + " | ".join(["---"] * len(conn_types)) + " |"
    lines = ["## Capability x connector matrix", "", header, divider]
    for cap in capabilities:
        if cap.orphaned:
            continue
        row = [cap.value, f"`{cap.method}`" if cap.method else ""]
        for conn_type in conn_types:
            row.append(cell.get((cap.value, conn_type), ""))
        lines.append("| " + " | ".join(row) + " |")
    lines.append("")
    lines.append(
        "Legend: `yes` = declared and backed by a live method; `cfg` = available "
        "only under the right configuration/backend (annotated, not resolved — "
        "`describe()` runs without a config)."
    )
    lines.append("")
    return "\n".join(lines)


def _render_capability_index(capabilities: tuple[CapabilityInfo, ...]) -> str:
    """Render the per-capability provider index (which types serve each)."""
    lines = ["## Capabilities", ""]
    for cap in capabilities:
        if cap.orphaned:
            continue
        providers = ", ".join(cap.provided_by) if cap.provided_by else "—"
        configurable = ", ".join(cap.configurable_in) if cap.configurable_in else "—"
        method = f"`{cap.method}`" if cap.method else "—"
        lines.append(f"- **{cap.value}** (method {method})")
        lines.append(f"  - provided by: {providers}")
        if cap.configurable_in:
            lines.append(f"  - configurable in: {configurable}")
    lines.append("")
    return "\n".join(lines)


def _render_seams(seams: Seams) -> str:
    """Render the seam manifest: Protocol seams, conventions, and template."""
    lines = ["## Extension seams", ""]
    lines.append(
        "To add a connector, place it at "
        f"`{seams.add_connector_template}` and implement the `BaseConnector` "
        "Protocol **structurally** (a bare class — do NOT subclass it)."
    )
    lines.append("")

    lines.append("### Protocol seams")
    lines.append("")
    for proto in seams.protocols:
        lines.append(f"#### `{proto.name}` ({proto.location})")
        if proto.doc:
            lines.append("")
            lines.append(proto.doc)
        lines.append("")
        for method in proto.methods:
            qualifier = "async " if method.is_async else ""
            doc = f" — {method.doc}" if method.doc else ""
            lines.append(f"- `{qualifier}{method.name}(...)`{doc}")
        lines.append("")

    lines.append("### Convention seams")
    lines.append("")
    lines.append(
        "Not reflectable Protocols — conventions an implementer follows, with "
        "where each one lives:"
    )
    lines.append("")
    for conv in seams.conventions:
        lines.append(f"- **{conv.name}** — {conv.note}")
        lines.append(f"  - location: `{conv.location_template}`")
    lines.append("")
    return "\n".join(lines)


def _render_config_summary(config_schema: dict[str, Any]) -> str:
    """Render a shape-only summary of the config schema's top-level keys."""
    lines = ["## Configuration schema (shape only)", ""]
    lines.append(
        "Top-level keys of `MachinaConfig` (from `model_json_schema()`). Shape "
        "only — never any configured values:"
    )
    lines.append("")
    properties = config_schema.get("properties", {})
    if isinstance(properties, dict):
        for key in sorted(properties):
            prop = properties[key]
            description = ""
            if isinstance(prop, dict):
                description = prop.get("description", "") or ""
            # First sentence only, keep the summary compact.
            summary = description.split(". ")[0].strip()
            if summary:
                lines.append(f"- `{key}` — {summary}")
            else:
                lines.append(f"- `{key}`")
    defs = config_schema.get("$defs", {})
    if isinstance(defs, dict) and defs:
        lines.append("")
        lines.append("Nested config objects: " + ", ".join(f"`{name}`" for name in sorted(defs)))
    lines.append("")
    return "\n".join(lines)


def _render_gaps(spine: Spine) -> str:
    """Render the known-gaps section (orphaned caps + the settings note)."""
    lines = ["## Known gaps", ""]
    gaps = spine.gaps
    by_value = {c.value: c for c in spine.capabilities}
    if gaps.orphaned_capabilities:
        lines.append(
            "Capabilities in the vocabulary that **no registered connector** "
            "provides (declared only by connectors absent from the default "
            "factory registry — e.g. `SimulatedSensorConnector`):"
        )
        lines.append("")
        for value in gaps.orphaned_capabilities:
            note = by_value[value].orphan_note if value in by_value else ""
            lines.append(f"- **{value}** — {note}" if note else f"- **{value}**")
        lines.append("")
    if gaps.settings_note:
        lines.append(gaps.settings_note)
        lines.append("")
    return "\n".join(lines)


